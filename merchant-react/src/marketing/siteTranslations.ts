const EN_TO_ZH = Object.fromEntries(
  Object.entries(marketingZhToEn).map(([zh, en]) => [en, zh]),
);

function translateValue(value: string, locale: SiteLocale) {
  const trimmed = value.trim();
  if (!trimmed)
    return value;

  const dictionary = locale === "en" ? marketingZhToEn : EN_TO_ZH;
  const translated = dictionary[trimmed];
  if (!translated)
    return value;

  return value.replace(trimmed, translated);
}

function shouldSkipTextNode(node: Node) {
  const parent = node.parentElement;
  if (!parent)
    return true;

  return ["SCRIPT", "STYLE", "NOSCRIPT", "TEXTAREA", "INPUT", "CODE", "PRE"].includes(parent.tagName);
}

export function applyStaticTranslations(locale: SiteLocale) {
  if (typeof document === "undefined" || !document.body)
    return;

  const nodeFilter = window.NodeFilter;
  const walker = document.createTreeWalker(document.body, nodeFilter.SHOW_TEXT, {
    acceptNode(node) {
      return shouldSkipTextNode(node) ? nodeFilter.FILTER_REJECT : nodeFilter.FILTER_ACCEPT;
    },
  });

  const textNodes: Text[] = [];
  while (walker.nextNode())
    textNodes.push(walker.currentNode as Text);

  textNodes.forEach(node => {
    const translated = translateValue(node.nodeValue ?? "", locale);
    if (translated !== node.nodeValue)
      node.nodeValue = translated;
  });

  document.querySelectorAll<HTMLElement>("[placeholder], [aria-label], [title], [alt]").forEach(element => {
    ["placeholder", "aria-label", "title", "alt"].forEach(attribute => {
      const value = element.getAttribute(attribute);
      if (!value)
        return;

      const translated = translateValue(value, locale);
      if (translated !== value)
        element.setAttribute(attribute, translated);
    });
  });
}
