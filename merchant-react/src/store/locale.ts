const WORKBENCH_CATALOG = { zh: ZH, en: EN }

const DICTS: Record<Locale, Dict> = mergeDicts(
  WORKBENCH_CATALOG,
  marketingCatalog,
  marketingPagesCatalog,
  accountCatalog,
  kefuCatalog,
  automationCatalog,
  composerCatalog,
  commonCatalog,
)

interface LocaleState {
  locale: Locale
  dicts: Record<Locale, Dict>
}

interface LocaleActions {
  isZh: () => boolean
  isEn: () => boolean
  init: () => void
  set: (loc: Locale) => void
  toggle: () => void
  applyHtmlLang: () => void
  t: (key: string, fallback?: string) => string
  tpl: (key: string, ...parts: string[]) => string
  tCopy: (copy: LocalizedCopy) => string
}

export const useLocaleStore = create<LocaleState & LocaleActions>()(
  persist(
    (set, get) => ({
      locale: 'zh',
      dicts: DICTS,
      isZh: () => get().locale === 'zh',
      isEn: () => get().locale === 'en',
      init: () => {
        migrateLegacyLocaleKeys()
        get().applyHtmlLang()
        syncLocaleToLegacyKeys(get().locale)
      },
      set: (loc) => {
        set({ locale: loc })
        get().applyHtmlLang()
        syncLocaleToLegacyKeys(loc)
      },
      toggle: () => get().set(get().locale === 'zh' ? 'en' : 'zh'),
      applyHtmlLang: () => {
        if (typeof document !== 'undefined')
          document.documentElement.lang = get().locale === 'zh' ? 'zh-CN' : 'en'
      },
      t: (key, fallback) => {
        const dict = DICTS[get().locale]
        if (key in dict) return dict[key]
        if (fallback !== undefined) return fallback
        return key.split('.').filter(Boolean).pop() || key
      },
      tpl: (key, ...parts) => {
        let s = get().t(key)
        parts.forEach((p, i) => {
          s = s.split(`{${i}}`).join(p)
        })
        return s
      },
      tCopy: (copy) => copy[get().locale],
    }),
    {
      name: 'xiaoone-merchant-locale',
    }
  )
)
